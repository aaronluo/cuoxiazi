package com.innovaee.eorder.module.exception;

public class STFException extends Exception {

	private static final long serialVersionUID = 1L;

	public STFException() {
		super();
	}

	public STFException(String message, Throwable cause) {
		super(message, cause);
	}

	public STFException(String message) {
		super(message);
	}

	public STFException(Throwable cause) {
		super(cause);
	}

}