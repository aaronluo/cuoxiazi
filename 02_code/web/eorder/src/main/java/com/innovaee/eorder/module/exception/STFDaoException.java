package com.innovaee.eorder.module.exception;

public class STFDaoException extends STFException {

	private static final long serialVersionUID = 1L;

	public STFDaoException() {
		super();
	}

	public STFDaoException(String message, Throwable cause) {
		super(message, cause);
	}

	public STFDaoException(String message) {
		super(message);
	}

	public STFDaoException(Throwable cause) {
		super(cause);
	}
}